#!/usr/bin/env node

// Catalogue USAA DOM structural markers (data-qa, data-testid,
// data-automation-id, stable IDs, semantic classes) on key pages.
//
// Uses the isolated usaa profile so we don't interfere with any
// running Amazon daemon. Non-destructive — read-only navigation.
//
// Pages probed:
//   /my/usaa                        — dashboard with account list
//   /my/checking/<acct>/activity    — transactions + "Export"
//   /my/statements                  — statements index
//   /my/inbox                       — inbox messages
//   /my/credit-card/<acct>/account-summary — CC billing detail

import { homedir } from "node:os";
import { join } from "node:path";
import { chromium } from "patchright";

const PROFILE_DIR = join(homedir(), ".pdpp", "profiles", "usaa");

const context = await chromium.launchPersistentContext(PROFILE_DIR, {
  headless: true,
  channel: "chrome",
  viewport: { width: 1280, height: 800 },
  args: ["--disable-features=DownloadBubble,DownloadBubbleV2,DownloadBubbleV3"],
});

interface ProbeAttrs {
  "data-automation-id": number | string;
  "data-component": number | string;
  "data-qa": number | string;
  "data-testid": number | string;
  "id=": number;
}

interface ProbeSample {
  attr: string | null;
  tag: string;
  text: string;
}

interface ProbeResult {
  attrs?: ProbeAttrs;
  bodyPreview?: string;
  error?: string;
  label?: string;
  samples?: {
    dataQa: ProbeSample[];
    dataTestId: ProbeSample[];
    dataAutomationId: ProbeSample[];
    dataComponent: ProbeSample[];
  };
  semanticIds?: string[];
  signinRedirect?: boolean;
  title?: string;
  url?: string;
}

async function probe(url: string, label: string): Promise<ProbeResult> {
  const page = await context.newPage();
  console.error(`[probe:${label}] ${url}`);
  try {
    await page.goto(url, { waitUntil: "domcontentloaded", timeout: 30_000 });
  } catch (e) {
    const m = e instanceof Error ? e.message : String(e);
    console.error(`  goto failed: ${m}`);
    await page.close();
    return { label, url, error: m };
  }
  // Wait ~5s for hydration
  await page.waitForTimeout(5000);

  const snap: ProbeResult = await page
    .evaluate((): ProbeResult => {
      const g = globalThis as Record<string, unknown>;
      const doc = g.document as {
        querySelectorAll: (s: string) => Array<{
          tagName: string;
          getAttribute: (n: string) => string | null;
          innerText?: string;
          id?: string;
        }>;
        title: string;
        body?: { innerText?: string };
      };
      const loc = g.location as { href: string };
      const all = (sel: string) => [...doc.querySelectorAll(sel)];
      const count = (sel: string): number | string => {
        try {
          return doc.querySelectorAll(sel).length;
        } catch {
          return "err";
        }
      };

      // Structural attribute counts across the whole page
      const attrs: ProbeAttrs = {
        "data-qa": count("[data-qa]"),
        "data-testid": count("[data-testid]"),
        "data-automation-id": count("[data-automation-id]"),
        "data-component": count("[data-component]"),
        "id=": doc.querySelectorAll("[id]").length,
      };

      // Sample a handful of each attribute kind to see what they look like
      const sample = (sel: string, limit = 15): ProbeSample[] =>
        all(sel)
          .slice(0, limit)
          .map((el): ProbeSample => {
            let attr: string | null = null;
            for (const a of [
              "data-qa",
              "data-testid",
              "data-automation-id",
              "data-component",
            ]) {
              const v = el.getAttribute(a);
              if (v) {
                attr = `${a}="${v}"`;
                break;
              }
            }
            return {
              tag: el.tagName,
              attr,
              text: (el.innerText || "")
                .replace(/\s+/g, " ")
                .trim()
                .slice(0, 80),
            };
          });

      return {
        url: loc.href,
        title: doc.title,
        // biome-ignore lint/performance/useTopLevelRegex: runs inside page.evaluate, serialized to browser
        signinRedirect: /\/ap\/signin|\/logon/.test(loc.href),
        attrs,
        samples: {
          dataQa: sample("[data-qa]"),
          dataTestId: sample("[data-testid]"),
          dataAutomationId: sample("[data-automation-id]"),
          dataComponent: sample("[data-component]"),
        },
        // Also capture IDs that look semantic (not autogenerated like "a-autoid-5")
        semanticIds: [...doc.querySelectorAll("[id]")]
          .map((el) => el.id ?? "")
          .filter(
            (id): boolean =>
              !!id &&
              // biome-ignore lint/performance/useTopLevelRegex: runs inside page.evaluate, serialized to browser
              !/^(a-autoid|ember|cdk|radix|react)-/i.test(id) &&
              id.length < 60
          )
          .slice(0, 30),
        bodyPreview: (doc.body?.innerText || "")
          .replace(/\s+/g, " ")
          .slice(0, 200),
      };
    })
    .catch(
      (e: unknown): ProbeResult => ({
        error: e instanceof Error ? e.message : String(e),
      })
    );

  await page.close();
  return { label, ...snap };
}

const results: Record<string, ProbeResult | undefined> = {};
results.dashboard = await probe("https://www.usaa.com/my/usaa", "dashboard");
// Try to grab the first account link, then drill into its activity page
if (
  results.dashboard &&
  !results.dashboard.signinRedirect &&
  !results.dashboard.error
) {
  try {
    const page = await context.newPage();
    await page.goto("https://www.usaa.com/my/usaa", {
      waitUntil: "domcontentloaded",
      timeout: 30_000,
    });
    await page.waitForTimeout(4000);
    const firstCheckingHref: string | null = await page.evaluate(() => {
      const g = globalThis as Record<string, unknown>;
      const doc = g.document as {
        querySelector: (s: string) => {
          getAttribute: (n: string) => string | null;
        } | null;
      };
      const a = doc.querySelector('a[href^="/my/checking"]');
      return a?.getAttribute("href") || null;
    });
    await page.close();
    if (firstCheckingHref) {
      const activityUrl = `https://www.usaa.com${firstCheckingHref.replace(/\/$/, "")}/activity`;
      results.activity = await probe(activityUrl, "activity");
    }
  } catch (e) {
    results.activity = {
      error: e instanceof Error ? e.message : String(e),
    };
  }
}
results.statements = await probe(
  "https://www.usaa.com/my/statements",
  "statements"
);
results.inbox = await probe("https://www.usaa.com/my/inbox", "inbox");

await context.close();
console.log(JSON.stringify(results, null, 2));
