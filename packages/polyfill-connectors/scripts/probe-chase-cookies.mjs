import { chromium } from 'playwright';
import { readFileSync } from 'node:fs';
import { homedir } from 'node:os';
import { join } from 'node:path';
const d = JSON.parse(readFileSync(join(homedir(), '.pdpp', 'browser-daemon.json'), 'utf8'));
const browser = await chromium.connectOverCDP(d.wsEndpoint);
const ctx = browser.contexts()[0];
const all = await ctx.cookies();
const chase = all.filter((c) => c.domain.includes('chase.com'));
console.log(`${chase.length} chase.com cookies:`);
for (const c of chase) console.log(`  ${c.domain}/${c.name} = ${c.value.slice(0, 40)}${c.value.length > 40 ? '...' : ''}`);
await browser.close();
