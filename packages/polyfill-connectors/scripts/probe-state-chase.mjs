import { chromium } from 'playwright';
import { readFileSync } from 'node:fs';
import { homedir } from 'node:os';
import { join } from 'node:path';
const d = JSON.parse(readFileSync(join(homedir(), '.pdpp', 'browser-daemon.json'), 'utf8'));
const browser = await chromium.connectOverCDP(d.wsEndpoint);
const ctx = browser.contexts()[0];
const page = ctx.pages().find((p) => p.url().includes('chase.com'));
console.log('url:', page.url());
console.log('title:', await page.title());
const text = (await page.locator('body').innerText().catch(() => '')).replace(/\s+/g, ' ');
console.log('body preview:', text.slice(0, 400));
console.log('---has-signoff---');
console.log(/sign off|log off|sign out|good (morning|afternoon|evening)|welcome/i.test(text));
console.log('---cookie count---');
console.log((await ctx.cookies('https://secure.chase.com')).length, 'cookies for secure.chase.com');
await browser.close();
