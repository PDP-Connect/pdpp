import { chromium } from 'playwright';
import { readFileSync } from 'node:fs';
import { homedir } from 'node:os';
import { join } from 'node:path';
const d = JSON.parse(readFileSync(join(homedir(), '.pdpp', 'browser-daemon.json'), 'utf8'));
const browser = await chromium.connectOverCDP(d.wsEndpoint);
const ctx = browser.contexts()[0];
const page = ctx.pages().find((p) => p.url().includes('amazon.com'));
console.log('url:', page.url());
console.log('title:', await page.title());
const text = (await page.locator('body').innerText().catch(() => '')).replace(/\s+/g, ' ');
console.log('body preview:', text.slice(0, 300));
console.log('cookies:', (await ctx.cookies('https://www.amazon.com')).length);
await browser.close();
